    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String partner = String.valueOf(cbPartner.getSelectedItem());

        Calendar calendarTrans = (Calendar) dPickerTransaksi.getModel().getValue();
        Date selectedDateTrans = calendarTrans.getTime();
        java.sql.Date tgl_transaksi = new java.sql.Date(selectedDateTrans.getTime());

        Calendar calendar = (Calendar) dPickerTransaksi.getModel().getValue();
        Date selectedDate = calendar.getTime();
        java.sql.Date tgl_nota = new java.sql.Date(selectedDate.getTime());
 
        try {
            String sql = "INSERT INTO transaksi_pengeluaran (id, nama_karyawan, nominal, partner, keterangan, tgl_dibuat, tgl_nota) VALUES "
                                    + "('"+tfId.getText()
                                    +"','"+ tfNamaKaryawan.getText()
                                    +"','"+ tfNominal.getText()
                                    +"','"+ partner
                                    +"','"+ tfKeterangan.getText()
                                    +"','"+ tgl_transaksi
                                    +"','"+ tgl_nota
                                    +"') ";
            java.sql.Connection conn = Connection.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();

            JOptionPane.showMessageDialog(null, "Data Berhasil disimpan!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal disimpan! "+e.getMessage());
        }

        loadData();
        clearData();
    }//GEN-LAST:event_btnSaveActionPerformed

