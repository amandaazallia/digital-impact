    private void loadData(){ 
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID"); 
        model.addColumn("Nominal"); 
        model.addColumn("Partner"); 
        model.addColumn("Nama Karyawan"); 
        model.addColumn("Tanggal Nota"); 
        model.addColumn("Tanggal Transaksi"); 
         
        try { 
            String sql = "select * from transaksi_pengeluaran";
            java.sql.Connection conn = (java.sql.Connection)Connection.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){  
                model.addRow(new Object[]{res.getString(1), 
                        res.getString(3), 
                        res.getString(4), 
                        res.getString(2), 
                        res.getString(6), 
                        res.getString(7)
                });
            }
            
            tblTransaction.setModel(model);
        } catch (Exception e) {
        }
    }
    
    public void dataPartnerCombobox(){ 
        List<String> partners = new ArrayList<String>();

        try {
            String sql = "select * from partners";
            java.sql.Connection conn = (java.sql.Connection)Connection.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while (res.next()) {
                partners.add(res.getString(2));
            }
            
            for (String item : partners) {
                cbPartner.addItem(item);
            } 
            
        } catch (Exception e) {
        }
    }
